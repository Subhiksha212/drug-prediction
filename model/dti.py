def predict_drugs(protein_seq):

    import torch
    import numpy as np
    from rdkit import Chem
    from rdkit.Chem import Draw
    from IPython.display import display

    protein_features = np.array(encode_protein(protein_seq)).reshape(1,-1)

    protein_tensor = torch.tensor(protein_features, dtype=torch.float32)
    drug_tensor = drug_features.float()

    protein_tensor = protein_tensor.repeat(len(drug_tensor),1)

    combined_features = torch.cat((protein_tensor, drug_tensor), dim=1)

    # 👇 keeps score between 0 and 1
    scores = torch.sigmoid(model(combined_features))

    top_indices = torch.topk(scores.squeeze(),10).indices
    top_scores = torch.topk(scores.squeeze(),10).values.detach().numpy()

    top_drugs = drugs.iloc[top_indices].copy()
    top_drugs["Score"] = top_scores

    for i,row in top_drugs.iterrows():

        print("Drug:", row["Drug_Name"])
        print("Score:", row["Score"])

        mol = Chem.MolFromSmiles(row["Canonical_SMILES"])
        display(Draw.MolToImage(mol,size=(250,250)))

